import java.util.*;
class ForloopDemo2{
	public static void main(String[] args) {
		System.out.println("Enter number");
		Scanner in =new Scanner(System.in);
		
		int n = in.nextInt();
		 int sum=0;
		for(int i=1; i<=n; i++){
			sum=sum+i;  // this will print adition of 0 to 10;
			System.out.println(sum );//git pull
		}
	}
}
