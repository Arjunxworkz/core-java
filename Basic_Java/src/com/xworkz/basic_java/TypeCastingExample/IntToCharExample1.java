package com.xworkz.basic_java.TypeCastingExample;

public class IntToCharExample1 {

	public static void main(String[] args) {
		int a=65; 
		int a2=90;
		char c=(char)a; 
		char c1=(char)a2; 
		
		System.out.println(a);  
		System.out.println(c); 
		System.out.println(a2);  
		System.out.println(c1); 

	}

}
